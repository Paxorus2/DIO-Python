# Variáveis

saldo = 200
saque = 200

# Operadores de Comparação

print("Igual: ", saldo == saque)                # Verifica se dois valores são iguais
print("Diferente: ", saldo != saque)            # Verifica se dois valores são diferentes
print("Maior que: ", saldo > saque)             # Verifica se o valor à esquerda é maior que o valor à direita
print("Maior ou igual: ", saldo >= saque)       # Verifica se o valor à esquerda é maior ou igual ao valor à direita
print("Menor que: ", saldo < saque)             # Verifica se o valor à esquerda é menor que o valor à direita
print("Menor ou igual: ", saldo <= saque)       # Verifica se o valor à esquerda é menor que ou igual ao valor à direita
print("É idêntico a: ", saldo is saque)         # Verifica se duas variáveis referenciam o mesmo objeto
print("Não é idêntico: ", saldo is not saque)   # Verifica se duas variáveis não referenciam o mesmo objeto