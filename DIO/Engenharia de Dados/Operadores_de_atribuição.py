# Operador de atribuição simples
x = 10
print("Valor inicial de x:", x)

# Operadores de atribuição compostos
x += 5
print("Após x += 5:", x)    # Equivalente a x = x + 5

x -= 3
print("Após x -= 3:", x)    # Equivalente a x = x - 3

x *= 2
print("Após x *= 2:", x)    # Equivalente a x = x * 2

x /= 4
print("Após x /= 4:", x)    # Equivalente a x = x / 4

x %= 3 
print("Após x %= 3:", x)    # Equivalente a x = x % 3

x **= 2
print("Após x **= 2:", x)   # Equivalente a x = x ** 2

x //= 2
print("Após x //= 2:", x)   # Equivalente a x = x // 2
